    private void save_buttonActionPerformed(java.awt.event.ActionEvent evt) {                                            
        String[] items = order_list.getItems();
        for(String n : items){
           order.addProduct(MenuItems.getID(n));
           DailyReports.setSolditemnumber(MenuItems.getID(n), 1);
        }
        DailyReportController.calculateExpense();
        DailyReportController.calculateIncome();
        DailyReportController.calculateRevenue();
        DailyReportController.calculateTips();     
        dispose(); 
    }                                           

