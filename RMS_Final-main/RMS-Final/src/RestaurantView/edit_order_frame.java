    public void menuCategory(){
        if(category=="Main Dishes"){
            menu_category.setText("Main Dishes");
             for(int i=1 ; i<=10 ; i++){
             if(MenuItems.getItem_type(i)== 1){
                menu_list.add(MenuItems.getName(i));
            }
        }
        }
        else if(category=="Beverages"){
            menu_category.setText("Beverages");
            for(int i=1 ; i<=10 ; i++){
             if(MenuItems.getItem_type(i)== 3){
                menu_list.add(MenuItems.getName(i));
            }
        }
        }
        else if(category=="Desserts"){
           menu_category.setText("Desserts");
           for(int i=1 ; i<=10 ; i++){
             if(MenuItems.getItem_type(i)== 2){
                menu_list.add(MenuItems.getName(i));
            }
        }
        }
    }
    

